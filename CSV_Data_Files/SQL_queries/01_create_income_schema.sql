-- Link each series to its matching CSV source file.
INSERT INTO income.series_source_file (series_id, source_file_id)
SELECT
  s.series_id,
  f.source_file_id
FROM income.income_series s
JOIN income.source_file f
  ON f.file_name = s.series_code || '.csv'
ON CONFLICT DO NOTHING;

-- Long view:
--   One row per series/date/value. This is the cleanest shape for R plots
--   where color/grouping is based on series_code.
CREATE OR REPLACE VIEW income.v_income_observations_long AS
SELECT
  d.observation_date,
  d.observation_year,
  d.observation_month,
  d.observation_quarter,
  s.series_code,
  s.series_name,
  s.frequency_code,
  s.units,
  io.observation_value,
  sf.file_name AS source_file
FROM income.income_observation io
JOIN income.income_series s
  ON s.series_id = io.series_id
JOIN income.observation_date d
  ON d.date_id = io.date_id
JOIN income.source_file sf
  ON sf.source_file_id = io.source_file_id;

-- Wide view:
--   One row per date with one column per series. Missing values are expected
--   because monthly, quarterly, and annual series do not share every date.
CREATE OR REPLACE VIEW income.v_income_observations_wide AS
SELECT
  observation_date,
  observation_year,
  observation_month,
  observation_quarter,
  MAX(CASE WHEN series_code = 'DSPI' THEN observation_value END) AS disposable_personal_income,
  MAX(CASE WHEN series_code = 'FODSP' THEN observation_value END) AS fodsp,
  MAX(CASE WHEN series_code = 'MEHOINUSA646N' THEN observation_value END) AS median_household_income,
  MAX(CASE WHEN series_code = 'PI' THEN observation_value END) AS personal_income,
  MAX(CASE WHEN series_code = 'RPI' THEN observation_value END) AS real_personal_income
FROM income.v_income_observations_long
GROUP BY
  observation_date,
  observation_year,
  observation_month,
  observation_quarter;

-- Annual summary:
--   One row per year/series with simple summary values for reporting.
CREATE OR REPLACE VIEW income.v_income_annual_summary AS
SELECT
  observation_year,
  series_code,
  series_name,
  AVG(observation_value) AS average_value,
  MIN(observation_value) AS minimum_value,
  MAX(observation_value) AS maximum_value,
  COUNT(*) AS observation_count
FROM income.v_income_observations_long
GROUP BY
  observation_year,
  series_code,
  series_name;
