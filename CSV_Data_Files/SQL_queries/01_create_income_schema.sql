-- PostgreSQL schema for the income CSV files.
-- Run this script before loading CSV data.
--
-- Correct result:
--   The database should store each income value once for each series/date pair.
--   Dates, series names, and source file information are kept in separate
--   tables so repeated text is not copied into every data row.
--
-- Main design:
--   income.income_series      = one row per income metric
--   income.observation_date   = one row per date
--   income.source_file        = one row per CSV file
--   income.series_source_file = junction between series and source files
--   income.income_observation = junction/fact table between series and dates

CREATE SCHEMA IF NOT EXISTS income;
CREATE SCHEMA IF NOT EXISTS income_stage;

-- Valid frequency values are stored once and reused by income_series.
-- This prevents spelling differences like monthly/month/Monthly.
CREATE TABLE IF NOT EXISTS income.series_frequency (
  frequency_code VARCHAR(16) PRIMARY KEY,
  frequency_name VARCHAR(32) NOT NULL UNIQUE
);

-- One row describes one income series, such as PI or RPI.
-- The actual time-series values are not stored here.
CREATE TABLE IF NOT EXISTS income.income_series (
  series_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  series_code VARCHAR(32) NOT NULL UNIQUE,
  series_name TEXT NOT NULL,
  frequency_code VARCHAR(16) NOT NULL,
  units TEXT NOT NULL,
  source_name TEXT NOT NULL,
  notes TEXT,
  CONSTRAINT income_series_frequency_fk
    FOREIGN KEY (frequency_code)
    REFERENCES income.series_frequency (frequency_code)
);

-- One row describes one CSV source file.
-- This helps track where each loaded value came from.
CREATE TABLE IF NOT EXISTS income.source_file (
  source_file_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  file_name TEXT NOT NULL UNIQUE,
  file_description TEXT
);

-- Junction table: connects a series to the file that supplied it.
-- This keeps the design expandable if a future file contains several series.
CREATE TABLE IF NOT EXISTS income.series_source_file (
  series_id INTEGER NOT NULL,
  source_file_id INTEGER NOT NULL,
  PRIMARY KEY (series_id, source_file_id),
  CONSTRAINT series_source_file_series_fk
    FOREIGN KEY (series_id)
    REFERENCES income.income_series (series_id),
  CONSTRAINT series_source_file_source_file_fk
    FOREIGN KEY (source_file_id)
    REFERENCES income.source_file (source_file_id)
);

-- One row per calendar date used by the data.
-- Year/month/quarter are stored here so queries do not recalculate them.
CREATE TABLE IF NOT EXISTS income.observation_date (
  date_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  observation_date DATE NOT NULL UNIQUE,
  observation_year SMALLINT NOT NULL,
  observation_month SMALLINT NOT NULL,
  observation_quarter SMALLINT NOT NULL,
  CONSTRAINT observation_month_check
    CHECK (observation_month BETWEEN 1 AND 12),
  CONSTRAINT observation_quarter_check
    CHECK (observation_quarter BETWEEN 1 AND 4)
);

-- Main fact/junction table.
-- The primary key forces one value per income series per date.
-- This is the rule that prevents duplicate rows in the correct schema.
CREATE TABLE IF NOT EXISTS income.income_observation (
  series_id INTEGER NOT NULL,
  date_id INTEGER NOT NULL,
  source_file_id INTEGER NOT NULL,
  observation_value NUMERIC NOT NULL,
  PRIMARY KEY (series_id, date_id),
  CONSTRAINT income_observation_series_fk
    FOREIGN KEY (series_id)
    REFERENCES income.income_series (series_id),
  CONSTRAINT income_observation_date_fk
    FOREIGN KEY (date_id)
    REFERENCES income.observation_date (date_id),
  CONSTRAINT income_observation_source_file_fk
    FOREIGN KEY (source_file_id)
    REFERENCES income.source_file (source_file_id)
);

-- Indexes make common joins and date filtering faster.
CREATE INDEX IF NOT EXISTS income_observation_date_idx
  ON income.income_observation (date_id);

CREATE INDEX IF NOT EXISTS income_observation_source_file_idx
  ON income.income_observation (source_file_id);

